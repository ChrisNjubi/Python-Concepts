# 1. Bitwise And (&) -> If both bits are equal to 1 then the result is 1 otherwise 0
# 2. Bitwise Or (|) -> If at least any one bit is 1, then the result is 1 otherwise 0
# 3. Bitwise XOR (^) -> If both bits are different in nature, then the result is 1 otherwise 0
# 4. Bitwise complement (~) -> -(n+1)

print("-------Bitwise &")

print(bin(45))
print(bin(47))
print(47 & 45)


print("-------Bitwise |")

print(bin(46))
print(bin(34))

print(46 | 34)

print("-------Bitwise ~")
print(~(-4))



print("-------Bitwise |")
print(46 ^ 34)
print(0b001100)